//Funcion declarativa
function miFuncion(){
    console.log('Saludos desde mi función');
}
miFuncion();
//Saludos desde mi función 

//Funcion flecha
const miFuncionFlecha = () =>{
    console.log('Saludos desde mi función flecha');
} //En este tipo de función no aplica hoisting.
miFuncionFlecha();
//Saludos desde mi función flecha
